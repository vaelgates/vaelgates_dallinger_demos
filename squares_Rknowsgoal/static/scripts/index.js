function startGame(){

  // increment the counter
  counterval = theta_cond_pairs.length-theta_cond_pairs_available.length
  countervalstr = String(counterval)+"/"+String(theta_cond_pairs.length);
  document.getElementById('counter').innerHTML = countervalstr;

  // clear board
  document.getElementById("replay").style.visibility="hidden";
  origBoard = Array.from(Array(9).keys());
  for( var i = 0; i < cells.length; i++){
    cells[i].innerText = ''; 
    cells[i].style.removeProperty('background-color');
    cells[i].bgColor = "white";
    cells[i].addEventListener('click', turnClick, false);
  }

  // reset movement number
  movenum = 0 
  firstmove = -1
  mostrecentmove = -1

}

function turnClick(square){
  // H takes a turn
  if( typeof origBoard[square.target.id] == 'number' ){
      turn(parseInt(square.target.id), Hplayersym);
      // check if game is over
      let gameWon = checkWin(origBoard);
      if( gameWon ) { resetGame()} 

      else {
        // let the R take a turn
        if(!allSquaresFilled()){ 
          //turn( bestSpot(), Rplayersym ); 
          turn( bestSpot_forGoalKnownR(), Rplayersym ); 
          // check if game is over
          let gameWon = checkWin(origBoard);
          if( gameWon ) {resetGame();}
        }
      }
    }
}

function turn(squareID, playersym){

  if (playersym==Hplayersym) { 
    document.getElementById(squareID).innerText = playersym;
    document.getElementById(squareID).bgColor="grey"; 
    // disable controls while it's the R's turn
    for( var i = 0; i < cells.length; i++){
      cells[i].removeEventListener('click', turnClick, false);}
  } else if (playersym==Rplayersym) {
    //add a time delay for the R playersym
    window.setTimeout(function(){ 
      document.getElementById(squareID).innerText = playersym;
      document.getElementById(squareID).bgColor="grey"; 
      // enable H controls again
      for( var i = 0; i < cells.length; i++){
        cells[i].addEventListener('click', turnClick, false);
      }
    },1000);
   }

  origBoard[squareID] = playersym;
  if (movenum == 0){
    firstmove = squareID
  }
  mostrecentmove = squareID
  movenum = movenum+1 //starts at 1


  // save information about each turn
  // save player type (H or R)
  var playertype
  if (playersym == '*'){
    playertype = "H"
  } else if (playersym == '^'){
    playertype = "R"
  }
  // create the info
  response = {"trial": counterval, "move_num": movenum, "squareID": squareID, "player": playertype};
  dallinger.createInfo(my_node_id, {
    contents: JSON.stringify(response),
    info_type: "Info"
  });
}

function getAllIndices(arr, val) {
    var indexes = [], i = -1;
    while ((i = arr.indexOf(val, i+1)) != -1){
        indexes.push(i);
    }
    return indexes;
}

function checkWin(board){
  // get the indices of the squares that have been played
  let plays = indicesForFilledSquares(board)

  var nplays = plays.length
  // check if the plays contain the desired winCombo (winCombo for the specified theta)
  if (plays.filter(function (elem) {
    return winCombos[theta].indexOf(elem) > -1;
      }).length == winCombos[theta].length){

    if (nplays != 3){
      bonus_earned = bonus_earned-.025 // MONICA need to add to Dallinger bonus in .py file  
    }

    //save trial info (this won't save if I put it in startGame(), not sure why)
    response = {"trial": counterval,
            "theta_cond_pair": theta_cond_pair,
            "theta_cond_pair_str": theta_cond_pairs_str[theta_cond_pair],
            "theta": theta};
    dallinger.createInfo(my_node_id, {
      contents: JSON.stringify(response),
      info_type: "Info"
    });

    // save final information
    response = {"trial": counterval, "nsquares": nplays}; //, "bonus": bonus_earned};
    dallinger.createInfo(my_node_id, {
      contents: JSON.stringify(response),
      info_type: "Info"
    });

    return gameWon = true


  }

  else gameWon = false
 }

